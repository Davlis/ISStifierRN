import React, { Component } from 'react';
import { Notifications } from 'expo';
import { Platform, StyleSheet, Text, View } from 'react-native';

// This refers to the function defined earlier in this guide
import { registerForPushNotificationsAsync } from '../utils'

export default class StartScreen extends Component {
  state = {
    notification: {
      origin: null,
      data: null
    },
  };

  componentDidMount() {
    registerForPushNotificationsAsync()
      .then(result => alert(`registerForPushNotificationsAsync::result : ${result}`))
      .catch(error => alert(`registerForPushNotificationsAsync::error : ${error}`))

    // Handle notifications that are received or selected while the app
    // is open. If the app was closed and then opened by tapping the
    // notification (rather than just tapping the app icon to open it),
    // this function will fire on the next tick after the app starts
    // with the notification data.
    this._notificationSubscription = Notifications.addListener(this._handleNotification);
  }

  _handleNotification = notification => {
    this.setState({ notification  });
  };

  render() {
    const { 
      notification: { origin, data } 
    } = this.state
    
    return (
      <View style={{ flex: 1, justifyContent: 'center', alignItems: 'center' }}>
        <Text>Origin: {origin === null ? 'No origin data available' : origin}</Text>
        <Text>Data: {data === null ? 'No data available' : JSON.stringify(data)}</Text>
      </View>
    );
  }
}


